document.addEventListener('DOMContentLoaded', function() {
    const tabLinks = document.querySelectorAll('.tab-nav-link');
    const tabPanes = document.querySelectorAll('.tab-pane');

    // Function to switch tab
    function switchTab(tabId) {
        tabLinks.forEach(link => {
            if(link.getAttribute('data-tab') === tabId) {
                link.classList.add('active');
            } else {
                link.classList.remove('active');
            }
        });

        tabPanes.forEach(pane => {
            if(pane.id === tabId) {
                pane.classList.add('active');
            } else {
                pane.classList.remove('active');
            }
        });
    }

    // Click Event Listener
    tabLinks.forEach(link => {
        link.addEventListener('click', () => {
            const tabId = link.getAttribute('data-tab');
            switchTab(tabId);
            // Update URL hash without jumping
            history.pushState(null, null, '#' + tabId);
        });
    });

    // Check Hash on Page Load (Auto-open tab)
    const hash = window.location.hash.substring(1); // remove #
    if (hash) {
        // Map common aliases (like transfer-rank) to actual ID (transfer-rules)
        let targetId = hash;
        if (hash === 'transfer-rank') targetId = 'transfer-rules';
        
        // If element exists, switch to it
        if (document.getElementById(targetId)) {
            switchTab(targetId);
            // Scroll to navigation area smoothly
            document.querySelector('.tab-nav').scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
    }
});
</script>
